/**
 * Studio Zero — V2.1 cli-tool scaffolder.
 *
 * Phase 9 V2.1 Batch 1 (Forge). Produces a Node CLI binary scaffold that
 * mirrors `apps/cli/`: a single-entrypoint binary with arg parsing,
 * subcommand dispatch, and a stable exit-code contract.
 *
 * Files:
 *   package.json (bin entry + commander dep)
 *   tsconfig.json
 *   src/index.ts
 *   src/commands/init.ts
 *   src/commands/run.ts
 *   README.md
 *   .gitignore
 */
import type { Brief } from "../../v2/types.js";
import type { ScaffolderInput, ScaffolderOutput } from "../types.js";

export function scaffoldCliTool(input: ScaffolderInput): ScaffolderOutput {
  const { brief, project_slug, generated_files } = input;

  const files: ScaffolderOutput["files"] = [];
  const pick = (path: string, fallback: string): string => {
    const gen = generated_files.get(path);
    return gen ?? fallback;
  };

  files.push({ path: "package.json", contents: packageJson(project_slug), pinned: true });
  files.push({ path: "tsconfig.json", contents: tsconfigJson(), pinned: true });
  files.push({ path: "src/index.ts", contents: indexTs(brief, project_slug) });
  files.push({ path: "src/commands/init.ts", contents: initCmdTs() });
  files.push({ path: "src/commands/run.ts", contents: runCmdTs() });
  files.push({ path: "README.md", contents: pick("README.md", readmeMd(brief, project_slug)) });
  files.push({ path: ".gitignore", contents: gitignore(), pinned: true });

  return {
    stack: "cli-tool",
    files,
    summary: `Node CLI scaffold for ${brief.project_name}. ${files.length} files, subcommand dispatch + stable exit codes. Audit-gated.`,
    install_instructions: [
      "1. `pnpm install`",
      `2. \`pnpm build\` then \`npx ${project_slug} --help\`.`,
    ].join("\n"),
  };
}

function packageJson(slug: string): string {
  return JSON.stringify(
    {
      name: slug,
      version: "0.1.0",
      private: false,
      type: "module",
      bin: { [slug]: "dist/index.js" },
      files: ["dist"],
      scripts: {
        build: "tsc -p tsconfig.json",
        dev: "tsx src/index.ts",
        test: "vitest run",
      },
      devDependencies: {
        "@types/node": "^22.10.0",
        tsx: "^4.21.0",
        typescript: "^5.7.0",
        vitest: "^2.1.0",
      },
      engines: { node: ">=20.18.0" },
    },
    null,
    2,
  );
}

function tsconfigJson(): string {
  return JSON.stringify(
    {
      compilerOptions: {
        target: "ES2022",
        module: "ES2022",
        moduleResolution: "bundler",
        strict: true,
        noUncheckedIndexedAccess: true,
        esModuleInterop: true,
        skipLibCheck: true,
        outDir: "./dist",
        rootDir: "./src",
        declaration: false,
        sourceMap: false,
        resolveJsonModule: true,
      },
      include: ["src/**/*.ts"],
      exclude: ["node_modules", "dist"],
    },
    null,
    2,
  );
}

function indexTs(brief: Brief, slug: string): string {
  return `#!/usr/bin/env node
/**
 * ${brief.project_name} — CLI entrypoint.
 *
 * Generated by Studio Zero (AI Act Art. 50 disclosure). See README.md.
 *
 * Exit codes (stable contract — do not change without a SemVer major):
 *   0  ok
 *   1  generic error
 *   2  argument parse error
 *   64 misuse of shell builtins
 */
import { runInit } from "./commands/init.js";
import { runRun } from "./commands/run.js";

async function main(): Promise<number> {
  const argv = process.argv.slice(2);
  const cmd = argv[0];
  if (!cmd || cmd === "--help" || cmd === "-h") {
    print(\`Usage: ${slug} <init|run> [args]\`);
    return 0;
  }
  switch (cmd) {
    case "init":
      return runInit(argv.slice(1));
    case "run":
      return runRun(argv.slice(1));
    default:
      print(\`Unknown command: \${cmd}\`);
      return 2;
  }
}

function print(s: string): void {
  process.stdout.write(s + "\\n");
}

main()
  .then((code) => process.exit(code))
  .catch((err: unknown) => {
    const msg = err instanceof Error ? err.message : String(err);
    process.stderr.write(\`error: \${msg}\\n\`);
    process.exit(1);
  });
`;
}

function initCmdTs(): string {
  return `export async function runInit(_args: string[]): Promise<number> {
  process.stdout.write("init: scaffolded a starter config at ./config.json\\n");
  return 0;
}
`;
}

function runCmdTs(): string {
  return `export async function runRun(_args: string[]): Promise<number> {
  process.stdout.write("run: no-op — wire your business logic here\\n");
  return 0;
}
`;
}


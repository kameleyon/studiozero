import type { Brief } from "../../v2/types.js";
import type { ScaffolderInput, ScaffolderOutput } from "../types.js";

export function scaffoldNodejsWorker(input: ScaffolderInput): ScaffolderOutput {
  const { brief, project_slug, generated_files } = input;

  const files: ScaffolderOutput["files"] = [];
  const pick = (path: string, fallback: string): string => {
    const gen = generated_files.get(path);
    return gen ?? fallback;
  };

  files.push({ path: "package.json", contents: packageJson(project_slug), pinned: true });
  files.push({ path: "tsconfig.json", contents: tsconfigJson(), pinned: true });
  files.push({ path: "src/index.ts", contents: indexTs(brief) });
  files.push({ path: "src/worker.ts", contents: workerTs() });
  files.push({ path: "src/health.ts", contents: healthTs() });
  files.push({ path: "src/abort-signal.ts", contents: abortSignalTs() });
  files.push({ path: "README.md", contents: pick("README.md", readmeMd(brief, project_slug)) });
  files.push({ path: ".env.example", contents: envExample() });
  files.push({ path: ".gitignore", contents: gitignore(), pinned: true });

  return {
    stack: "nodejs-worker",
    files,
    summary: `Node.js worker scaffold for ${brief.project_name}. ${files.length} files, pg-boss-shape queue consumer + AbortController + health probes. Audit-gated.`,
    install_instructions: [
      "1. `pnpm install`",
      "2. Copy `.env.example` to `.env`; fill in DATABASE_URL.",
      "3. `pnpm start` — the worker boots and starts polling the queue.",
    ].join("\n"),
  };
}


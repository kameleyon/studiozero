function indexTs(brief: Brief): string {
  return `/**
 * ${brief.project_name} — worker entrypoint.
 *
 * Generated by Studio Zero (AI Act Art. 50 disclosure). See README.md.
 */
import { startWorker } from "./worker.js";
import { startHealth } from "./health.js";

const ctrl = new AbortController();
process.on("SIGTERM", () => ctrl.abort("sigterm"));
process.on("SIGINT", () => ctrl.abort("sigint"));

await startHealth(ctrl.signal);
await startWorker(ctrl.signal);
`;
}

function workerTs(): string {
  return `/**
 * pg-boss-shape queue consumer. Replace the connection string in .env.
 */
export interface JobPayload {
  type: string;
  data: Record<string, unknown>;
}

export async function startWorker(signal: AbortSignal): Promise<void> {
  if (!process.env.DATABASE_URL) {
    throw new Error("DATABASE_URL not set — see .env.example.");
  }
  // Real impl uses pg-boss. Stubbed loop here so the scaffold runs out of the box.
  while (!signal.aborted) {
    await sleep(1000, signal).catch(() => undefined);
  }
}

function sleep(ms: number, signal: AbortSignal): Promise<void> {
  return new Promise<void>((resolve, reject) => {
    const t = setTimeout(resolve, ms);
    signal.addEventListener(
      "abort",
      () => {
        clearTimeout(t);
        reject(new Error("aborted"));
      },
      { once: true },
    );
  });
}
`;
}

function healthTs(): string {
  return `import { createServer } from "node:http";

export function startHealth(signal: AbortSignal): Promise<void> {
  const server = createServer((req, res) => {
    if (req.url === "/health" || req.url === "/healthz") {
      res.writeHead(200, {
        "Content-Type": "application/json",
        "X-AI-Generated": "studio-zero",
      });
      res.end(JSON.stringify({ ok: true, service: "worker" }));
      return;
    }
    res.writeHead(404);
    res.end();
  });
  const port = Number(process.env.HEALTH_PORT ?? 8081);
  return new Promise<void>((resolve) => {
    server.listen(port, () => {
      signal.addEventListener("abort", () => server.close(), { once: true });
      resolve();
    });
  });
}
`;
}

function abortSignalTs(): string {
  return `/**
 * Helper to chain a parent + per-step signal. Returns a controller whose
 * signal aborts when either source aborts. Used by every long-running
 * step in the worker so cancel propagates correctly.
 */
export function chainSignals(parent: AbortSignal, child?: AbortSignal): AbortController {
  const ctrl = new AbortController();
  const onAbort = (): void => ctrl.abort();
  if (parent.aborted) ctrl.abort();
  else parent.addEventListener("abort", onAbort, { once: true });
  if (child) {
    if (child.aborted) ctrl.abort();
    else child.addEventListener("abort", onAbort, { once: true });
  }
  return ctrl;
}
`;
}


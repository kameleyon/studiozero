/**
 * Studio Zero — V2 Build mode GitHub repo seeder.
 *
 * Phase 9 V2 Batch 1 (Forge). After the bundle is assembled AND the
 * audit gate has decided PASS or PASS WITH FIXES, this module creates
 * a fresh GitHub repo on the customer's account, pushes the bundle, and
 * creates milestones + labeled issues from each layer's deliverables.
 *
 * Customer onboarding: returns a `magic_link` token the web app pairs
 * with the build row so the customer's first repo visit triggers an
 * onboarding tour explaining the seeded structure.
 *
 * Cross-refs:
 *   - PRD §7.3 step 5 (Seeded GitHub repo deliverable)
 *   - architecture/decisions.md ARCH-D1 (we always go through the
 *     GitHub App install, never raw OAuth)
 *   - apps/runner/src/build/pr-opener.ts (V1.5 — same GitHub client
 *     contract, reused here)
 *
 * Failure modes:
 *   - GitHub App uninstalled mid-flight → return seedResult with
 *     `repo_created=false` + reason; web app shows the bundle anyway,
 *     just no repo link.
 *   - Repo name collision → append `-{n}` suffix and retry once.
 *   - Per-finding label missing → create on the fly.
 */
import type { LayerOutput, RoadmapBundle } from "./types.js";

/** Minimal GitHub client interface — implemented in the runner against
 *  Octokit, mocked in tests. Mirrors the pr-opener.ts shape. */
export interface GhSeederClient {
  createRepo(args: {
    owner: string;
    name: string;
    description: string;
    private: boolean;
  }): Promise<{ html_url: string; default_branch: string }>;
  putFile(args: {
    owner: string;
    repo: string;
    path: string;
    content: string;
    message: string;
    branch: string;
  }): Promise<{ sha: string }>;
  createMilestone(args: {
    owner: string;
    repo: string;
    title: string;
    description: string;
  }): Promise<{ number: number }>;
  createIssue(args: {
    owner: string;
    repo: string;
    title: string;
    body: string;
    labels: string[];
    milestone?: number;
  }): Promise<{ number: number; html_url: string }>;
}

export interface SeedInput {
  bundle: RoadmapBundle;
  /** GitHub owner (org or user) the customer has authorized the App
   *  to act on. Resolved server-side from the App install record. */
  owner: string;
  /** Slugged project name; collisions are retried with a numeric
   *  suffix by the caller. */
  repoSlug: string;
  /** Visibility — V2 default is private. */
  private?: boolean;
}

export interface SeedResult {
  repo_created: boolean;
  html_url?: string;
  default_branch?: string;
  milestones_created: number;
  issues_created: number;
  reason?: string;
}

/** Required output preferences for repo seeding — guard at caller. */
export function shouldSeedRepo(
  output_preference: RoadmapBundle["layer_outputs"][number] extends never
    ? never
    : string,
): boolean {
  return (
    output_preference === "roadmap-docs-repo" ||
    output_preference === "roadmap-docs-repo-scaffold"
  );
}

const DOC_FILES: Array<{
  path: string;
  key: keyof RoadmapBundle["documents"];
}> = [
  { path: "README.md", key: "readme_md" },
  { path: "ARCHITECTURE.md", key: "architecture_md" },
  { path: "docs/roadmap.md", key: "roadmap_md" },
  { path: "docs/prd.md", key: "prd_md" },
  { path: "docs/voice.md", key: "voice_md" },
  { path: "docs/decisions.md", key: "decisions_md" },
  { path: "docs/risks.md", key: "risks_md" },
  { path: "docs/cogs.md", key: "cogs_md" },
  { path: "docs/channels.md", key: "channels_md" },
  { path: "brand/tokens.json", key: "brand_tokens_json" },
];

/** Convert a layer output into a list of GitHub issues — one per artifact. */
function issuesForLayer(layer: LayerOutput): Array<{
  title: string;
  body: string;
  labels: string[];
}> {
  const layerLabel = `layer:${layer.layer}`;
  const out: Array<{ title: string; body: string; labels: string[] }> = [];
  out.push({
    title: `[${layer.layer}] ${layer.agent} — initial work`,
    body:
      `Owner: ${layer.agent}\n\nSummary:\n${layer.summary}\n\nProduced as part of Studio Zero V2 Build mode.\n` +
      `\n---\n*AI Act Art. 50 disclosure: this issue was generated by Studio Zero. See /ai-system-card.*`,
    labels: [layerLabel, "studio-zero:seeded"],
  });
  for (const art of layer.artifacts) {
    out.push({
      title: `[${layer.layer}] Implement ${art.name}`,
      body:
        `Owner: ${layer.agent}\nArtifact: \`${art.name}\` (${art.kind})\n\nFull spec is committed at the repo root or under \`docs/\`.\n` +
        `\n---\n*AI Act Art. 50 disclosure: this issue was generated by Studio Zero.*`,
      labels: [layerLabel, "studio-zero:seeded"],
    });
  }
  return out;
}

export async function seedRepo(
  input: SeedInput,
  gh: GhSeederClient,
): Promise<SeedResult> {
  const { bundle, owner, repoSlug } = input;

  let repo: { html_url: string; default_branch: string };
  try {
    repo = await gh.createRepo({
      owner,
      name: repoSlug,
      description: `${(bundle.documents.readme_md.split("\n")[0] ?? bundle.brief_ref).replace(/^#\s*/, "")} — seeded by Studio Zero`,
      private: input.private ?? true,
    });
  } catch (err) {
    return {
      repo_created: false,
      milestones_created: 0,
      issues_created: 0,
      reason: err instanceof Error ? err.message : "create_repo_failed",
    };
  }

  // Push documents in series so commits stay ordered + readable in git log.
  for (const f of DOC_FILES) {
    try {
      await gh.putFile({
        owner,
        repo: repoSlug,
        path: f.path,
        content: bundle.documents[f.key],
        message: `chore(seed): ${f.path}`,
        branch: repo.default_branch,
      });
    } catch {
      // Continue — one missing doc shouldn't abort the whole seed.
    }
  }

  // Milestones from strategy layer's roadmap (heuristic: one milestone per
  // H2 in roadmap.md). Falls back to single milestone per layer if no H2s.
  const milestoneTitles = extractMilestonesFromRoadmap(
    bundle.documents.roadmap_md,
  );
  const milestoneNumbers: number[] = [];
  for (const title of milestoneTitles) {
    try {
      const ms = await gh.createMilestone({
        owner,
        repo: repoSlug,
        title,
        description: `Milestone seeded by Studio Zero for ${bundle.brief_ref}.`,
      });
      milestoneNumbers.push(ms.number);
    } catch {
      /* ignore individual milestone failures */
    }
  }

  // Issues from every layer's outputs.
  let issuesCreated = 0;
  for (const layer of bundle.layer_outputs) {
    const issues = issuesForLayer(layer);
    for (const issue of issues) {
      try {
        await gh.createIssue({
          owner,
          repo: repoSlug,
          title: issue.title,
          body: issue.body,
          labels: issue.labels,
          milestone: milestoneNumbers[0],
        });
        issuesCreated += 1;
      } catch {
        /* continue */
      }
    }
  }

  return {
    repo_created: true,
    html_url: repo.html_url,
    default_branch: repo.default_branch,
    milestones_created: milestoneNumbers.length,
    issues_created: issuesCreated,
  };
}

/** Extract H2 headings from a markdown roadmap to use as milestone titles. */
export function extractMilestonesFromRoadmap(md: string): string[] {
  const lines = md.split(/\r?\n/);
  const titles: string[] = [];
  for (const line of lines) {
    const m = /^##\s+(.+)$/.exec(line);
    const captured = m?.[1];
    if (captured) titles.push(captured.trim().slice(0, 240));
  }
  if (titles.length === 0) titles.push("M0 — discovery");
  return titles.slice(0, 8);
}

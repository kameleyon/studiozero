/**
 * Fingerprint a pubkey for telemetry / audit_logs. NOT a cryptographic
 * commitment — just a human-friendly identifier so a Cipher reviewing
 * audit_logs can correlate rows to the runbook's rotation entries
 * without leaking the full key material into log surfaces.
 *
 * Defined here (next to the constants) rather than in manifest-verify.ts
 * so that tools/cli-manifest-sign.ts can import it without pulling in
 * the verifier's network code.
 */
export function pubkeyFingerprint(pubkeyB64: string): string {
  // First 12 chars of base64 → 9 bytes of entropy; collision-resistant
  // for a set of ~5 keys (one current + ~4 historical). NOT to be used
  // for any trust decision — purely for log correlation.
  return pubkeyB64.slice(0, 12);
}

/**
 * Studio Zero — embedded Ed25519 manifest-signing public key.
 *
 * Phase 9 M3 Batch 2 (Cipher Fix-3c). This file is the runtime trust
 * root for CLI manifest verification per `architecture/cli-manifest-
 * signing.md` §3.2. It is intentionally tiny: the rotation mechanism is
 * "edit this file + npm publish", per the rotation runbook.
 *
 * Invariants (load-bearing):
 *   - The CURRENT_PUBKEY is the ONLY key the build tool
 *     (tools/cli-manifest-sign.ts) signs current manifests with.
 *   - LEGACY_PUBKEYS contains every previous active pubkey that is still
 *     inside its 30-day grace window. After the grace window closes,
 *     entries are REMOVED in a patch-bump release (rotation runbook
 *     §5.1 step 10).
 *   - Both arrays are read by `manifest-verify.ts` at every CLI startup
 *     AND mirrored to the server's runtime_config row that
 *     `apps/web/lib/cli-manifest-verifier.ts` reads.
 *
 * Format: each entry is the SPKI-shaped Ed25519 public key encoded as
 * base64 (without PEM armor). To convert PEM → this constant:
 *
 *     const pem = readFileSync("pubkey.pem", "utf-8");
 *     const b64 = pem
 *       .replace(/-----(BEGIN|END) PUBLIC KEY-----/g, "")
 *       .replace(/\s+/g, "");
 *
 * The well-known URL (https://studio-zero.com/.well-known/cli-manifest-
 * pubkey.pub) serves the PEM-armored form of CURRENT_PUBKEY for
 * out-of-band verifiers; that file is regenerated and committed at the
 * same time this constant changes.
 *
 * IF YOU ARE EDITING THIS FILE:
 *   - The rotation runbook (`architecture/secrets-rotation-runbook.md`
 *     §1.7) describes the full ceremony. Do not edit this file
 *     outside that ceremony or an incident response — every change to
 *     these constants is a security-relevant audit event.
 *   - The CI manifest-sign step reads the matching private key from
 *     Jo's 1Password (one-shot, never persisted). If you edit the
 *     pubkey here without coordinating the privkey rotation, the next
 *     CLI release will refuse to verify its own manifest.
 *   - Cipher reviews every change; CODEOWNERS gate on this file.
 */

